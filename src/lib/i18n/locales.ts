export function resolveBrowserLocale(lang: string | undefined): Locale {
  if (!lang) return "en";
  const lower = lang.toLowerCase();
  if (lower.startsWith("pt")) return "pt-BR";
  if (lower.startsWith("fr")) return "fr";
  if (lower.startsWith("es")) return "es";
  return "en";
}

